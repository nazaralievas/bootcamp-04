# разбор домашки: Один игрок загадывает число, второй отгадывает. Если отгадал, то выводится "Вы угадали!!!!"
# если второй игрок назвал число меньше загаданного, то выводится "Слишком мало" и в ином случае "Слишком много"
player1 = int(input("Загадайте число от 0 до 10: "))
player2 = int(input("Отгадайте число от 0 до 10: "))

if player1 == player2:
    print("Вы угадали!!!!")
elif player2 > player1:
    print("Слишком много")
else:
    print("Слишком мало")


