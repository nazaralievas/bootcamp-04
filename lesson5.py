# практика: пишем программу, которая проверяет длину комментария (должен быть не длиннее 50 символов) с помощью функции len()
# и проверяет на содержание плохих слов, в данном случае слова durak
bad_word = "durak"
comment = "                            hey hello you DuRaK      "

if len(comment.strip()) < 50: # проверяем длину
    if not bad_word in comment.lower(): # с помощью метода lower() все буквы в комменте сделали маленькими и если слова durak нет в комменте, то разрешаем к публикации
        print("Комментарий опубликован")
    else:
        print("Удали плохое слово")
else:
    print("Комментарий слишком длинный, сократи.")


