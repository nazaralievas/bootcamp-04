# но чтобы вам не приходилось вручную искать индекс нужной буквы, вы можете воспользоваться методом find()
# в котором вы указываете нужную букву, а он вам возвращает индекс этой буквы

print(text.find('д'))  # возвращает индекс буквы "д" который равен 9
print(text.find('е'))  # возвращает индекс буквы "е" который равен 15
print(text.find('н'))  # возвращает индекс буквы "н" который равен 19
print(text.find('ь'))  # возвращает индекс буквы "ь" который равен 17

# узнав нужные индексы, мы их принтуем:
