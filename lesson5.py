# задача: найти количество букв "С" и "с" в этом тексте
text = "Скажи-ка дядя ведь не даром, Москва, спалённая пожаром"

# способ первый: считаем сколько больших букв "С" и сколько маленьких букв "с" в тексте и плюсуем
print(text.count("с") + text.count("С"))

# способ второй с использованием метода lower(). Все буквы в тексте станут маленькими и мы сможем посчитать сколько в нём букв "с"
print(text.lower().count('с'))


