# давайте сделаем функцию, которая не просто приветствует пользователя, а создаёт ему email на основе переданных данных
# у нашей функции будет два параметра: name и surname (имя и фамилия)
# эти name и surname он затем переводит в нижний регистр, добавляет к ним знак @ и _
def nickname_maker(name, surname):
    print('@' + name.lower() + '_' + surname.lower())


