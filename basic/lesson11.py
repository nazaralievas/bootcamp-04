# давайте встроим этот код в нашу функцию
def nickname_maker(name, surname):
    from random import randint
    num = randint(1000, 9999)
    print('@' + name.lower() + '_' + surname.lower() + '_' + str(num)) # знак + склеивает строки между собой, поэтому num мы превращаем в строку

# вызываем нашу функцию
nickname_maker('Saadat', 'Nazaralieva')
# и получаем вывод в консоли: @saadat_nazaralieva_3664


